#CADENAS DE TEXTO

#Las cadenas de texto se pueden delimitar, indiferentemente, por comillas simples o dobles, 
#siempre que se utilicen las mismas.
print("Hola mundo")
print('Hola mundo')

#Si utilizamos comillas triples, las cadenas podrán ocupar más de una línea.
print("""Esto es un ejemplo de una cadena triple
      buah, es una cadena triple
      encima triple""")

#Los caracteres especiales empiezan siempre por una contrabarra "\".

#Pintar una comilla doble dentro de comillas dobles:
print("Esto es una \" comilla doble")

#Lo mismo para comillas simples:
print('Esto es una \' comilla simple')

#Podemos realizar un SALTO DE LINEA con "\n":
print("Patata frita \npatata frita \npero que patata frita")

#Por último, podremos tabular con "\t":
print("Texto 1 \t Texto 1 pero más a la derecha \t Te vas a romper el cuello")

#Como vemos en el ejemplo de línea, es importante tener en cuenta que LOS ESPACIOS CUENTAN.


